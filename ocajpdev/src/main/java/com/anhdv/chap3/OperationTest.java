package com.anhdv.chap3;

public class OperationTest {

	public static void main(String[] args) {
		int age = 30;
		System.out.println(age);
		// Postdecrement
		System.out.println(age--);
		// Postincrement
		System.out.println(age++);
		// Predecrement
		System.out.println(--age);
		// Preincrement
		System.out.println(++age);

	}

}
